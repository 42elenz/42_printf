#include "printf.h"

void	ft_pputstr(char *s, int *re)
{
	int	i;

	i = 0;
	if (s == NULL)
	{
		write(1, "(null)", 6);
		*re += 6;
	}
	if (s != NULL)
	{
		while (s[i])
		{
			(*re)++;
			write(1, &s[i], 1);
			i++;
		}
	}
}
